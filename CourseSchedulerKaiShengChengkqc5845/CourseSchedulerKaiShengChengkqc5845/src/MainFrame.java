    private void DropStudentSubmitButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_DropStudentSubmitButtonActionPerformed
        // TODO add your handling code here:
        dropStudentTextArea.setText("");
        currentStudent = (StudentEntry)dropStudentChooseComboBox.getSelectedItem();
        dropStudentTextArea.append((dropStudentChooseComboBox.getSelectedItem()) + " has been dropped from the list of students.\n\n");
        
        for (String semester : SemesterQueries.getSemesterList())
        {
            String scheduledCourses = "";
            String droppedCourses = "";
            
            for (ScheduleEntry c : ScheduleQueries.getScheduleByStudent(semester, currentStudent.getStudentID()))
            {
                ScheduleQueries.dropStudentScheduleByCourse(semester, currentStudent.getStudentID(), c.getCourseCode());
                droppedCourses += (dropStudentChooseComboBox.getSelectedItem() + (c.getStatus().equals("w") ? " has been dropped from the waitlist for ": " has been dropped from ") + c.getCourseCode() + "\n");
                
                ArrayList<ScheduleEntry> waitlisted = ScheduleQueries.getWaitListedStudentsByClass(semester, c.getCourseCode());
                if (waitlisted.size() > 0)
                {
                    ScheduleQueries.updateScheduleEntry(waitlisted.get(0));
                    StudentEntry newStudent = StudentQueries.getStudent(waitlisted.get(0).getStudentID());
                    scheduledCourses += newStudent.getFirstName() + ", " + newStudent.getLastName() + " " + waitlisted.get(0).getStudentID() + " has been scheduled into " + waitlisted.get(0).getCourseCode() + "\n";
                }
            }
            if (!droppedCourses.isEmpty() || !scheduledCourses.isEmpty())
            {
                dropStudentTextArea.append("For Semester: " + semester + "\n");
                dropStudentTextArea.append(droppedCourses);
                dropStudentTextArea.append(scheduledCourses);
            }
        }
        
        StudentQueries.dropStudent(currentStudent.getStudentID());
        rebuildDropStudentChooseComboBox();
        rebuildDropClassChooseStudentComboBox();
        rebuildScheduleSelectStudentComboBoxes();
        rebuildselectStudentComboBoxes();
    }//GEN-LAST:event_DropStudentSubmitButtonActionPerformed

