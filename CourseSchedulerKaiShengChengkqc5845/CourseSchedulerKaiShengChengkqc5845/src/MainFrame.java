    private void scheduleClassSubmitButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_scheduleClassSubmitButtonActionPerformed
        // TODO add your handling code here:
        String class1 = (String)selectClassComboBox.getSelectedItem();
        currentStudent = (StudentEntry)selectStudentComboBox.getSelectedItem();
        String status = "s";
        if (ClassQueries.getClassSeats(currentSemester,class1)<= ScheduleQueries.getScheduledStudentCount(currentSemester,class1))
        {
            status = "w";
            scheduleClassStatusLabel.setText(currentStudent.getFirstName()+ " " + currentStudent.getLastName()+" has been waitlisted for " + class1);
        }
        else
        {
            scheduleClassStatusLabel.setText(currentStudent.getFirstName()+ " " + currentStudent.getLastName()+" has been scheduled for " + class1);
        }
        
        ScheduleQueries.addScheduleEntry(
            new ScheduleEntry(
                currentSemester,class1,currentStudent.getStudentID(),status,new java.sql.Timestamp(System.currentTimeMillis())
            ));
    }//GEN-LAST:event_scheduleClassSubmitButtonActionPerformed

